class tubeFeed implements TubeFeedObject{
	constructor(kcal:number,protein:number,water:number,name:string,reference:string,micros:Micros){
		this._kcal=kcal;
		this._protein=protein;
		this._water=water;
		this._name=name;
		this._reference=reference;
		this._micros=micros;
	}
	_kcal = 0;
	_protein = 0;
	_water = 0;
	_name = "";
	_reference = "";
	_micros = {
        
	};

	get kcal(){
		return this._kcal;
	}
	get protein(){
		return this._protein;
	}
	get water(){
		return this._water;
	}
	get name(){
		return this._name;
	}
	get reference(){
		return this._reference;
	}
	get micros(){
		return this._micros;
	}
    
}

